package pseudoCodigo;

public class pseudoCodigo {

	public static void main(String[] args) {
		
		int c;
		c = 1;
		int sum=0;
		
		//HACER
		do{
		sum+=c;
		c++;
		//HASTA QUE (c<101)
		}while(c<101);
		
		System.out.println(sum);

	}// aca termina el método main

}// aca termina la clase pseudoCodigo
