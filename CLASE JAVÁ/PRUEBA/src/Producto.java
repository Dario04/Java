
public class Producto {
    public int Mozzarella;
	public int Tomate;
    public int Provolone;
    public int Jamon;
    public int Acelga;
    public int SalsaBlanca;
    public int QuesoRayado;

    
	public Producto(int mozzarella, int tomate, int provolone, int jamon, 
			int acelga, int salsaBlanca, int quesoRayado) {
		super();
		Mozzarella = mozzarella;
		Tomate = tomate;
		Provolone = provolone;
		Jamon = jamon;
		Acelga = acelga;
		SalsaBlanca = salsaBlanca;
		QuesoRayado = quesoRayado;
	}
	}

